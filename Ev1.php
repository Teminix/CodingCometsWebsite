<section id="1980">
<br><br><br>
<p><span class="sub-title">1982</span>
First genetically modified plant cell

Scientists working at Monsanto Company became the first in the world to genetically modify a plant cell. The team used Agrobacterium to introduce a new gene into the petunia plant and announced their achievement the following year. Within five years, Monsanto researchers planted their first outdoor trials of a genetically modified crop – tomatoes that were resistant to Roundup agricultural herbicide, insects, or viruses. The Agrobacterium method first used in 1982 is still in use today by Monsanto scientists and by other companies’ scientists.</p>
