	<p><span class="sub-title">1960s</span>:
The Green Revolution
In the mid-1940s U.S. Vice President Henry Wallace spearheaded a program to help developing nations feed their growing populations. The program included four scientists, one of whom was Dr. Norman Borlaug.
Borlaug started a growing process that allowed plants to thrive with new irrigation and crop management techniques. By the 1960s, the benefits of what was nicknamed the “Green Revolution” were apparent when successful new wheat varieties were made available in countries across the globe.</p>
</section>
<section id="1970">
<br><br><br>
<p><span class="sub-title">1974</span>
Roundup  agricultural herbicide released
Monsanto developed a new herbicide using glyphosate as the active ingredient. Glyphosate-based herbicide is used by farmers to control weeds in their crops. Roundup also made its way into lawn and garden products, which allow landowners to kill weeds along sidewalks, driveways, gardens, and fences.</p>

